# Вспомогательные функции

def extract_numeric(value):
    if pd.isna(value): return None
    match = re.search(r'\d+\.?\d*', str(value))
    return float(match.group()) if match else None

def parse_torque(torque_str):
    if pd.isna(torque_str) or not isinstance(torque_str, str): return None, None
    text = torque_str.lower().replace(',', '').strip()
    torque_value = None
    torque_unit = 'nm'

    t_match = re.search(r'(\d+(?:\.\d+)?)\s*(?:nm|kgm|n·m|kg·m)', text)
    if t_match:
        torque_value = float(t_match.group(1))
        if 'kgm' in t_match.group(0) or 'kg·m' in t_match.group(0):
            torque_value *= 9.80665
    else:
        t_before = re.search(r'(\d+(?:\.\d+)?)(?:@|/|at)', text)
        if t_before: torque_value = float(t_before.group(1))
        if 'kgm' in text or 'kg·m' in text: torque_value *= 9.80665

    rpm_pattern = r'(\d+)-(\d+)\s*rpm|rpm\s*(\d+)-(\d+)|(\d+)\s*rpm|rpm\s*(\d+)'
    rpm_matches = re.findall(rpm_pattern, text)
    rpm_numbers = [int(num) for m in rpm_matches for num in m if num]
    rpm_value = min(rpm_numbers) if rpm_numbers else None

    return (round(torque_value, 2) if torque_value else None, rpm_value)

def add_luxury_feature(df, luxury_list):
    df = df.copy()
    df['brand'] = df['name'].apply(lambda x: str(x).split()[0])
    df['is_luxury'] = df['brand'].apply(lambda x: 1 if x in luxury_list else 0)
    return df

