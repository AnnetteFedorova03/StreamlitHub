    medians = {col: df_train[col].median() for col in target_cols}
    for col in target_cols:
        df_train[col] = df_train[col].fillna(medians[col])
        df_test[col] = df_test[col].fillna(medians[col])

    df_train['engine'] = df_train['engine'].astype('int64')
    df_train['seats'] = df_train['seats'].astype('int64')
    df_test['engine'] = df_test['engine'].astype('int64')
    df_test['seats'] = df_test['seats'].astype('int64')

    # Feature engineering
    luxury_list = ['BMW', 'Mercedes-Benz', 'Audi', 'Lexus', 'Jaguar', 'Land', 'Volvo', 'Jeep', 'MG', 'Kia']
    max_year = df_train['year'].max()

    def engineering_full(df):
        df = add_luxury_feature(df, luxury_list)
        df['year_squared'] = df['year'] ** 2
        df['max_power_squared'] = df['max_power'] ** 2
        df['hp_per_liter'] = df['max_power'] / (df['engine'] + 1)
        df['km_per_year'] = df['km_driven'] / (max_year - df['year'] + 1)
        df['is_multi_owner'] = df['owner'].isin(['Third Owner', 'Fourth & Above Owner']).astype(int)
        df['is_high_mileage'] = (df['km_driven'] > 150000).astype(int)
        df['km_driven_log'] = np.log1p(df['km_driven'])
        return df

    df_train = engineering_full(df_train)
    df_test = engineering_full(df_test)
    df_train['selling_price_log'] = np.log1p(df_train['selling_price'])

    # Обучение модели
    y_train_log = df_train['selling_price_log']
    X_train = df_train.drop(columns=['name', 'brand', 'selling_price', 'selling_price_log'])
    X_train_final = pd.get_dummies(X_train, drop_first=True)
    f_names = X_train_final.columns

    scaler = StandardScaler()
    X_train_scaled = scaler.fit_transform(X_train_final)

    model = Ridge(alpha=0.1)
    model.fit(X_train_scaled, y_train_log)

