st.set_page_config(page_title="Car Price Predictor", layout="wide")
st.sidebar.title("Car Price App")

train_up = st.sidebar.file_uploader("1. Загрузите train.csv", type="csv")
test_up = st.sidebar.file_uploader("2. Загрузите test.csv", type="csv")

